uruk: A command line tool for setting up a machine

The purpose of uruk is to enable the setting up of a machine via a
command line interface. It does this by automatically downloading
and installing packages using a variety of package managers, and
moving fonts and backgrounds into the appropriate places.

Commands: -

install:- Installs a series of pacakges for your OS.
uninstall:- Removes uruk from your system.
help:- Displays the help page (this one).
configure:- Uses configuration files to style your OS and apps.
fonts:- Installs fonts on your machine
background:- Adds backgrounds into your images folder.
update:- Updates uruk on your machine.
clone:- Clones all repositories for a GitHub user.
pull:- Pulls all repositories for a GitHub user.

